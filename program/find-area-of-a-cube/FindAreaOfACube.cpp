// Programme to find the area of a cube.

#include <bits/stdc++.h>
#define ll long long // for typing ease.
using namespace std;

int main(){
    // Taking input;
    ll length;
    cout<<"Enter the side of the cube: ";
    cin>>length;

    //printing output;
    cout<<"The Surface area of the cube is: "<<6*(length*length)<<endl;
    
    return 0;
}

