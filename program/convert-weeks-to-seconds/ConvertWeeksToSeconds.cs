// Write a C# program to convert weeks to seconds

// 1 week = 7 days = 168 hours = 10080 minutes = 604800 seconds

// Input  : 5
// Output : 3024000

using System;
namespace Sample {
   class Demo {
      static void Main(string[] args) {
         int num1 = 5;
         int num2 = num1 * 604800;
         
         Console.WriteLine("Total Seconds :  {0}", num2); 
      }
   }
}

