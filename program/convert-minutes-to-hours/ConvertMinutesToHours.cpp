// This programme converts minutes to hours
#include <bits/stdc++.h>
#define ll long long // for typing ease.
using namespace std;
int main(){
    // Taking user input
    cout<<"Enter the minutes :";
    ll min; cin>>min;

    // Displaying output
    cout<<min<<" minutes = ";
    cout<<min/60.0<<" hour(s)"<<endl;  // since 60 min = 1hour
}

