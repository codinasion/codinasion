// Sum of N natural numbers Time complexity O(1) and space Complexity O(1);

#include <bits/stdc++.h>
#define ll long long
using namespace std;

int main(){
    ll n;   cin>>n;
    ll sum = (n*(n+1))/2; // Mathematical formula for Sum of N natural numbers.
    cout<<sum<<endl;
    return 0;
}

