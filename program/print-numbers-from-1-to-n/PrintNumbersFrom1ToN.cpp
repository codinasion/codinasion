// Programme to print numbers from 1 to N
#include <bits/stdc++.h>
#define ll long long // for typing ease;
using namespace std;
int main(){
    // Taking user input
    cout<<"Enter N: ";
    ll n;   cin>>n;
    
    //printing numbers from 1 to N;
    cout<<"Printing numbers from 1 to "<<n<<"..."<<endl;
    for(int i=1;i<=n;i++){
        cout<<i<<" ";
    }

    return 0;
}

