// To convert Seconds to hours
#include <bits/stdc++.h>
#define ll long long // for typing ease.
using namespace std;
int main(){
    //Taking input.
    cout<<"Enter the seconds : ";
    ll secs;    cin>>secs;

    //Displaying output
    cout<<secs<<" seconds are "<<secs/3600<<" hour(s)"<<endl;
    return 0;
}

